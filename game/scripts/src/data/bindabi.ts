export const GetBindParent = (abi: string) => {
	let result = abi
	Object.entries(bindabiList).forEach(v => {
		v[1].forEach(v2 => {
			if (v2.abi == abi) result = v[0]
		})
	})
	return result
}

export const GetBindChildCount = (abi: string) => {
	let result = 0
	if (Object.entries(bindabiList).find(v => v[0] == abi) == undefined) return result
	Object.entries(bindabiList).find(v => v[0] == abi)[1].forEach(v => {
		if (v.type == bindabiType.bonus) {
			result++
		}
	})
	return result
}

export const GetHeroBindCount = (hero: CDOTA_BaseNPC_Hero) => {
	let result = 0
	for (let i of [0, 1, 2, 5]) {
		Object.entries(bindabiList).find(v => v[0] == hero.GetAbilityByIndex(i).GetAbilityName())[1].forEach(v => {
			if (v.type == bindabiType.bonus) {
				result++
			}
		})
	}
	return result
}

